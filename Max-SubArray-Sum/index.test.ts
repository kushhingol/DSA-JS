import { maxSubArraySum } from ".";

describe("Test Max-SubArray-Sum", () => {
  test.each`
    nums                                | expected
    ${[-2, 1, -3, 4, -1, 2, 1, -5, 4]}  | ${6}
    ${[1]}                              | ${1}
    ${[0]}                              | ${0}
    ${[-1]}                             | ${-1}
    ${[-2147483647]}                    | ${-2147483647}
    ${[-2, -1]}                         | ${-1}
    ${[-2, 1]}                          | ${1}
    ${[-2, -1, -3]}                     | ${-1}
    ${[-2, -1, -3, -4]}                 | ${-1}
    ${[-2, -1, -3, -4, -1]}             | ${-1}
    ${[-2, -1, -3, -4, -1, -2]}         | ${-1}
    ${[-2, -1, -3, -4, -1, -2, -1]}     | ${-1}
    ${[-2, -1, -3, -4, -1, -2, -1, -5]} | ${-1}
    ${[5, 4, -1, 7, 8]}                 | ${23}
    ${[5, 4, -1, 7, 8, 9]}              | ${32}
  `("maxSubArraySum($nums) should return $expected", ({ nums, expected }) => {
    expect(maxSubArraySum(nums)).toBe(expected);
  });
});
